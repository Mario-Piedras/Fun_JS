console.log("El valor de a = ", a)

//Operdaores de comparación
//Los operadores de comparación sirven para comparar cantidades o valores entre variables.

/* 
= es el operador de asignación
> es el operador de mayor que
< es el operador de menor que
== es el operador de igual que
!= es el operdaor de diferente que
>= es el operador de mayor o igual que
<= es el operador de menor o igual que
*/

// Se declaran las varables
let valorUno, valorDos; 
//Utilidad de la asignación
valorUno = 20;
valorDos = 10;
//Operador mayor que >
document.writeln(valorUno>valorDos, "<br>");
console.log(valorUno>valorDos);
//EL sistema verificara si valorUno es mayor que el valorDos y devlvera un valor booleano, es decir: verdadero o falso (True o false).

//Operador mayor que <
document.writeln(valorUno<valorDos, "<br>");
console.log(valorUno<valorDos);
//EL sistema verificara si valorUno es menor el que valorDos y devolvera un valor booleano, es decir: verdadero o falso (True o false).

//Operador igual que ==
document.writeln(valorUno==valorDos, "<br>");
console.log(valorUno==valorDos);
//EL sistema verificara si valorUno es igual que el valorDos y devolvera un valor booleano, es decir: verdadero o falso (True o false).

//Operador diferente que !=
document.writeln(valorUno!=valorDos, "<br>");
console.log(valorUno!=valorDos);
//EL sistema verificara si valorUno es diferente que valorDos y devolvera un valor booleano, es decir: verdadero o falso (True o false).

//Operador mayor o igual que >=
document.writeln(valorUno>=valorDos, "<br>");
console.log(valorUno>=valorDos);
//EL sistema verificara si valorUno es mayor o igual que valorDos y devolvera un valor booleano, es decir: verdadero o falso (True o false).

//Operador mayor que <=
document.writeln(valorUno<=valorDos, "<br>");
console.log(valorUno<=valorDos);
//EL sistema verificara si valorUno es menor o igual que valorDos y devolvera un valor booleano, es decir: verdadero o falso (True o false).